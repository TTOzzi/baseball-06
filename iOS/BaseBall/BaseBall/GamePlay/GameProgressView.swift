import UIKit

class GameProgressView: UIView {
    override init(frame: CGRect) {
        super.init(frame: frame)
        setProperties()
    }
    
    required init?(coder: NSCoder) {
        super.init(coder: coder)
        setProperties()
    }
    
    func setProperties() {
        layer.borderWidth = 1
        layer.borderColor = UIColor.gray.cgColor
    }
}
