import UIKit

class PitchingResultDataSource: NSObject, UITableViewDataSource {
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return 10
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: PitchingResultCell.identifier, for: indexPath) as? PitchingResultCell else {
            return UITableViewCell()
        }
        return cell
    }
}
